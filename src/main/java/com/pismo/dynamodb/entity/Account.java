package com.pismo.dynamodb.entity;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBRangeKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;

@DynamoDBTable(tableName = "pismo-account")
public class Account {

    private String account_id;
    private String sk;
    private String documentNumber;
    private String amount;
    private String event_date;
    private String description;

    public Account() {
    }

    public Account(String documentNumber, String sk) {
        this.sk = sk;
        this.documentNumber = documentNumber;
    }

    @DynamoDBHashKey(attributeName = "id")
    @DynamoDBAutoGeneratedKey
    public String getId() {
        return account_id;
    }

    @DynamoDBRangeKey(attributeName = "sk")
    public String getSK() {
        return sk;
    }

    @DynamoDBAttribute(attributeName = "document_umber")
    public String getDocumentNumber() {
        return documentNumber;
    }

    public void setId(String id) {
        this.account_id = id;
    }

    public void setSK(String sk) {
        this.sk = sk;
    }

    public void setDocumentNumber(String cost) {
        this.documentNumber = cost;
    }

}
